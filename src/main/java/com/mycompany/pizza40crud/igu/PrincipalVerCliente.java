    private void btnEditarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarClienteActionPerformed
        if (tablaCliente.getRowCount() > 0) {
            if (tablaCliente.getSelectedRow() != -1) {
                String _id = String.valueOf(tablaCliente.getValueAt(tablaCliente.getSelectedRow(), 0));
                PrincipalModificarCliente patallaModif = new PrincipalModificarCliente(_id);
                patallaModif.setVisible(true);
                patallaModif.setLocationRelativeTo(null);
                this.dispose();
                
            } else {
                JOptionPane.showMessageDialog(null, "Selecciona un cliente para Modificar.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No hay clientes para Modificar.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnEditarClienteActionPerformed

