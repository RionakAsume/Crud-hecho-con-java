    private void btnEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarClienteActionPerformed
        if (tablaCliente.getRowCount() > 0) {
            if (tablaCliente.getSelectedRow() != -1) {
                int respuesta = JOptionPane.showConfirmDialog(null, "¿Estás seguro de que deseas eliminar este cliente?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
                if (respuesta == JOptionPane.YES_OPTION) {
                    String _id = String.valueOf(tablaCliente.getValueAt(tablaCliente.getSelectedRow(), 0));
                    try {
                        clientes.eliminarCliente(_id);
                        JOptionPane.showMessageDialog(null, "Cliente eliminado exitosamente.", "Eliminación exitosa", JOptionPane.INFORMATION_MESSAGE);
                        // Actualizar la tabla después de la eliminación
                        cargarTablas();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Error al eliminar el cliente: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }

            } else {
                JOptionPane.showMessageDialog(null, "Selecciona un cliente para eliminar.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "No hay clientes para eliminar.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarClienteActionPerformed

