    private void btnClienteGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClienteGuardarActionPerformed
        String nombre=txtClientesNombre.getText();
        String direccion=txtClientesDireccion.getText();
        String telefono=txtClientesTelefono.getText();
        String email=txtClientesEmail.getText();
        String fechaRegistro=txtClientesFecharegistro.getText();

        clientes.Insert(nombre, direccion,telefono,email,fechaRegistro);
        
        

        JOptionPane optionPane=new JOptionPane("Se guardo exitosamente");
        optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        JDialog dialog = optionPane.createDialog("Guerdo Exitoso");
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }//GEN-LAST:event_btnClienteGuardarActionPerformed

